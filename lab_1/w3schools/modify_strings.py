# uppercase
a = "Hello, World!"
print(a.upper())

# lowercase
a = "Hello, World!"
print(a.lower())

# remove whitespace
a = " Hello, World! "
print(a.strip()) # returns "Hello, World!"

# replace string
a = "Hello, World!"
print(a.replace("H", "J"))

# split string
a = "Hello, World!"
print(a.split(",")) # returns ['Hello', ' World!']